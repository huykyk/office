    private void drawingBoard1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_drawingBoard1MousePressed
        this.drawingBoard1.shape_list = this.shape_list;
            if(evt.getButton()==MouseEvent.BUTTON1){
                switch(tool){
                    case TOOL_LINE:
                        cur_shape = new Tool_Line(evt.getX(), evt.getY(), cur_color);
                        shape_list.add(cur_shape);
                        this.drawingBoard1.repaint();
                        break;
                    case TOOL_CIRCLE:
                        cur_shape = new Tool_Circle(evt.getX(), evt.getY(), cur_color, is_fill);
                        shape_list.add(cur_shape);
                        this.drawingBoard1.repaint();
                        break;
                    case TOOL_OVAL:
                        cur_shape = new Tool_Oval(evt.getX(), evt.getY(), cur_color, is_fill);
                        shape_list.add(cur_shape);
                        this.drawingBoard1.repaint();
                        break;
                    case TOOL_POLYGON:
                        cur_shape = new Tool_Polygon(evt.getX(), evt.getY(), cur_color, is_fill);
                        shape_list.add(cur_shape);
                        this.drawingBoard1.repaint();
                        break;
                }
            }else if(evt.getButton()==MouseEvent.BUTTON3){
                if(cur_shape!=null){
                    switch(tool){
                    case TOOL_POLYGON:
                        cur_shape.processMouseEvent(evt, IShape.MOUSE_RIGHT_PRESSED);
                        this.drawingBoard1.repaint();
                        break;
                    }
                }
            }

    }//GEN-LAST:event_drawingBoard1MousePressed

    private void drawingBoard1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_drawingBoard1MouseReleased
         if(cur_shape!=null){
            if(evt.getButton()==MouseEvent.BUTTON1){
                cur_shape.processMouseEvent(evt, IShape.MOUSE_LEFT_RELEASED);
                this.shape_list.add(cur_shape);
                this.cur_shape = null;
                this.drawingBoard1.repaint();
            }
        }
    }//GEN-LAST:event_drawingBoard1MouseReleased

