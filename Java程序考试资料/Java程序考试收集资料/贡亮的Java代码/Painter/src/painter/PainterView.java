    private void jMenuItem1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuItem1MousePressed
       JFileChooser chooser = new JFileChooser();
        if(JFileChooser.APPROVE_OPTION==chooser.showSaveDialog(null)){
            String path = chooser.getSelectedFile().getPath();
            try{
                FileOutputStream fos = new FileOutputStream(path);
                ObjectOutputStream oos = new ObjectOutputStream(fos);

                oos.writeObject(shape_list);

                oos.close();
            }catch(IOException ex){
                System.out.println(ex.getStackTrace());
            }
        }
    }//GEN-LAST:event_jMenuItem1MousePressed

    private void jMenuItem2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuItem2MousePressed
        System.out.println("saving...");
        JFileChooser chooser = new JFileChooser();
        if(JFileChooser.APPROVE_OPTION==chooser.showOpenDialog(null)){
            String path = chooser.getSelectedFile().getPath();
            try{
                FileInputStream fos = new FileInputStream(path);
                ObjectInputStream oos = new ObjectInputStream(fos);

                ArrayList<IShape> shapes = null;
                shapes = (ArrayList<IShape>) oos.readObject();
                shape_list = shapes;
                this.drawingBoard1.shape_list = shape_list;
                this.drawingBoard1.repaint();

                oos.close();
            }catch(IOException ex){
                System.out.println(ex.getStackTrace());
            }catch (ClassNotFoundException ex) {
                    Logger.getLogger(PainterView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItem2MousePressed

